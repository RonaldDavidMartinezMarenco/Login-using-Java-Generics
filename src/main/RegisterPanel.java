    public RegisterPanel(main frame) {
        this.frame = frame;
        initComponents();
        
        cmdRegister.addActionListener(e -> {
            String user = txtUser.getText().trim();
            String pass = new String(txtPass.getPassword());
            String tipo = (String) cmdClient.getSelectedItem();
            
            if (user.isEmpty() || pass.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingresa usuario y contrase√±a.");
                return;
            }
              if (frame.userExist(user)) {
                JOptionPane.showMessageDialog(this, "El usuario ya existe. Intenta con otro nombre.");
                return;
            }

            Login nuevoUsuario = tipo.equals("Cliente")
                    ? new Cliente(user, pass)
                    : new Admin(user, pass);

            frame.userRegister(nuevoUsuario);
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente.");
        });
    }

